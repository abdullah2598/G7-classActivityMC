package com.example.mcclasswork;

public class Dua
{
    String name;
    String count;
    String date;

    public String getName() {
        return name;
    }

    public String getCount() {
        return count;
    }

    public String getDate() {
        return date;
    }


    public void setName(String name) {
        this.name = name;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
